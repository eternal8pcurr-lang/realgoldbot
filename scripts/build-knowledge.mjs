// Build step: reads the two RealGold markdown files and bundles them into a
// JavaScript module the backend can use. This keeps the knowledge files OUT of
// the public website (so visitors can't read your internal guardrails) while
// still letting you edit plain .md files and re-run `npm run knowledge`.
//
// You normally never run this by hand — `npm run dev` and the deploy step run it
// for you. It produces functions/api/_knowledge.js.

import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");

// The source-of-truth knowledge files (edit these any time).
const GUARDRAILS_FILE = join(root, "realgold_knowledge.md");
const CONTENT_FILE = join(root, "realgold_content_1_dst_1031.md");
const COMPANY_FILE = join(root, "realgold_content_2_company.md");

const guardrails = readFileSync(GUARDRAILS_FILE, "utf8");
const content = readFileSync(CONTENT_FILE, "utf8");
const company = readFileSync(COMPANY_FILE, "utf8");

// Pull the "Guiding Questions" out of Part 8 so the website can show them as
// clickable starters. We look for lines like:  - "What is RealGold...?"
function extractGuidingQuestions(text) {
  const lines = text.split("\n");
  let inSection = false;
  const questions = [];
  for (const line of lines) {
    const isHeading = /^#{1,6}\s/.test(line);
    if (isHeading) {
      // Enter the section when we hit the "GUIDING QUESTIONS" heading, leave it
      // at the next heading.
      inSection = /guiding questions/i.test(line);
      continue;
    }
    if (!inSection) continue;
    const m = line.match(/^\s*-\s*"(.+?)"\s*$/);
    if (m) questions.push(m[1]);
  }
  return questions;
}

const guidingQuestions = extractGuidingQuestions(guardrails);

const out = `// AUTO-GENERATED by scripts/build-knowledge.mjs — do not edit by hand.
// Edit realgold_knowledge.md / realgold_content_1_dst_1031.md instead,
// then run:  npm run knowledge
export const GUARDRAILS = ${JSON.stringify(guardrails)};
export const CONTENT = ${JSON.stringify(content)};
export const COMPANY = ${JSON.stringify(company)};
export const GUIDING_QUESTIONS = ${JSON.stringify(guidingQuestions, null, 2)};
`;

const outPath = join(root, "functions", "api", "_knowledge.js");
mkdirSync(dirname(outPath), { recursive: true });
writeFileSync(outPath, out, "utf8");

console.log(
  `Knowledge bundled: ${guardrails.length} + ${content.length} + ${company.length} chars, ` +
    `${guidingQuestions.length} guiding questions -> functions/api/_knowledge.js`
);
